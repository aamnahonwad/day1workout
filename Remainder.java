class Remainder {
    public static void main(String[] args) {

        int num1 = 17;   // Dividend
        int num2 = 5;    // Divisor

        int remainder = num1 % num2;

        System.out.println("Remainder when " + num1 + 
                           " is divided by " + num2 + 
                           " is: " + remainder);
    }
}