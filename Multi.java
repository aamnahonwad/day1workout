class Multi{
    public static void main(String[] args){
        int i=5,j=1,res=0,n=0;
        for(i=5;i<=n;i++){
            for(j=1;j<=n;j++){
                res=i*j;
                System.out.println("the multiple of the given numbers is" + i + "x" + j + res);
            }
        }
    }
}